# range() method

# using the range function to get the sequence of numbers from 1 to 10

for i in range(11):
    print(i)

# using the range function to ger the sequence of numbers between given range

for i in range(5,15):
    print(i)

# using the range function to get the sequence of the numbers with the no equally spaced by a factor of 3 between 5 and 15

for i in range(5,15,3):
    print(i)


